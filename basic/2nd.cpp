#include<iostream>
using namespace std;
int main(){
    const double PI = 3.14;                           //const will fix the value of pi
    double radius = 12;
    double circumference = 2*PI*radius;
 

    cout << "radius =  " << radius  << " cm" <<endl;

    cout <<"circumference of circle = " << circumference << " cm";



    return 0;



}

    
